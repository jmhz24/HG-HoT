def Parallel_Lines_corresponding_angles(angle):
    #This function uses the property that corresponding angles are equal when two lines are parallel.
    return angle

def Parallel_Lines_alternate_interior_angles(angle):
    #This function uses the property that alternate interior angles are equal when two lines are parallel.
    return angle

def Parallel_Lines_consecutive_interior_angles_180(angle):
    #This function uses the property that consecutive interior angles are supplementary when two lines are parallel.
    return 180 - angle
