def Symmetry_equal_line(line):
    # This function uses the property that in symmetrical shapes, corresponding lengths are equal.
    return line

def Symmetry_equal_angle(angle):
    # This function uses the property that in symmetrical shapes, corresponding angle are equal.
    return angle

def Symmetry_equal_sub_angle(whole_angle):
    # This function is commonly used in folding problems. The angles formed after folding are equal due to symmetry. Knowing the total angle, the folded angle can be calculated.
    return (1 / 2) * whole_angle
