def Rotation_equal_line(line):
    # This function uses the property that isometry: Rotation does not change the distance between any two points on a shape.
    return line

def Rotation_equal_angle(angle):
    # This function uses the property that conformality: Rotation preserves the measure of the interior angles of a shape.
    return angle

def Rotation_calculate_arc_length(radius, central_angle):
    # This function uses the property that calculation of the arc length formed by rotation.
    return (central_angle * math.pi * radius) / 180