def Regular_Polygon_calculate_each_interior_angle(number_line):
    # This function is used to calculate the degree measure of each angle in a regular polygon.
    return (number_line - 2) * 180 / number_line

def Regular_Polygon_calculate_central_angle(number_line):
    # This function is used to calculate the degree measure of the central angle of a regular polygon.
    return 360 / number_line

def Regular_Polygon_equal_angle(angle_line):
    # This function uses the property that in a regular polygon, all sides are equal in length and all interior angles are equal.
    return angle_line
