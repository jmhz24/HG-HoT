def Vertical_perpendicular():
    # This function uses the property that when a vertical line intersects the line it is perpendicular to, the Angle at the intersection is 90 degrees.
    return 90

def Vertical_supplementary_angles_90(angle):
    # This function uses the property that the two lines are perpendicular to each other, and in a right triangle composed of these two vertical lines and another line, the sum of the other two angles except the right Angle is 90 degrees.
    return 90 - angle